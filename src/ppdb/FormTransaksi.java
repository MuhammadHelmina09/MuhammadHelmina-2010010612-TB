    private void tampilkan() {
        int row =tbTransaksi.getRowCount();
        for(int a=0; a<row;a++){
            model.removeRow(0);
        
        }
        try {
            Connection cn = DriverManager.getConnection("jdbc:mysql://localhost/db_ppdb2","root","");
            ResultSet rs =cn.createStatement().executeQuery("SELECT * FROM transaksi");
            while(rs.next()){
                String data [] = {rs.getString(1),rs.getString(2),rs.getString(3),rs.getString(4),rs.getString(5)};
                model.addRow(data);
            }
        } catch (SQLException ex) {
            Logger.getLogger(FormTransaksi.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
