    private void tbTransaksiMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbTransaksiMouseClicked
        int i = tbTransaksi.getSelectedRow();
        
        if(i>-1){
            txtNoT.setText(model.getValueAt(i, 0).toString());
            cbNis.setSelectedItem(model.getValueAt(i, 1).toString());
            txtNama.setText(model.getValueAt(i, 2).toString());
            cbPembayaran.setSelectedItem(model.getValueAt(i, 3).toString());
            txtHarga.setText(model.getValueAt(i, 4).toString());
        }
    }//GEN-LAST:event_tbTransaksiMouseClicked

